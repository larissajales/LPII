package agenda;
import java.util.Date;


public class Agenda {
    private Date data;
    private String anotacoes;

    public Date getData() {
        return data;
    }

    public String getAnotacoes() {
        return anotacoes;
    }

    public void setAnotacoes(final String anotacoes) {
        this.anotacoes = anotacoes;
    }

    public void setData(final Date data) {
        this.data = data;
    }

}
    
    

